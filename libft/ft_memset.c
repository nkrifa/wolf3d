#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*src;

	src = (char*)s;
	i = 0;
	while (i < n)
	{
		src[i] = c;
		i++;
	}
	return (s);
}
