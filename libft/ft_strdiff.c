char		ft_strdiff(char *s1, char *s2)
{
	int i;

	while (*s1)
	{
		i = 0;
		while (*s1 != s2[i])
		{
			if (!s2[i])
				return (*s1);
			i++;
		}
		s1++;
	}
	return (0);
}
